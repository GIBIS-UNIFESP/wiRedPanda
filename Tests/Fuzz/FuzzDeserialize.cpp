// libFuzzer calls this to mutate one corpus entry.
extern "C" size_t LLVMFuzzerCustomMutator(
    uint8_t *Data, size_t Size, size_t MaxSize, unsigned int Seed)
{
    // libFuzzer is declared (but not defined) here so we can call it.
    extern size_t LLVMFuzzerMutate(uint8_t *, size_t, size_t);

    if (Size == 0 || MaxSize == 0)
        return LLVMFuzzerMutate(Data, Size, MaxSize);

    std::mt19937 rng(Seed);
    const int op = std::uniform_int_distribution<int>(0, 8)(rng);

    switch (op) {
    case 0: { // patch_version: write a known version field at byte 4
        const auto &vp = kVersions[rng() % std::size(kVersions)];
        const size_t patchOff = 4; // right after "WPCF"
        if (Size >= patchOff + sizeof(vp.bytes) && MaxSize >= patchOff + sizeof(vp.bytes)) {
            std::memcpy(Data + patchOff, vp.bytes, sizeof(vp.bytes));
            return Size;
        }
        break;
    }
    case 1: { // patch_map_key: splice a known key at a random byte offset
        const auto &key = kMapKeys[rng() % std::size(kMapKeys)];
        // Find how long this key is (4 byte-len header + key bytes)
        const uint32_t keyDataLen = (uint32_t(key[0])<<24)|(uint32_t(key[1])<<16)
                                   |(uint32_t(key[2])<<8)|key[3];
        const size_t totalLen = 4 + keyDataLen;
        const size_t pos = Size > 1 ? std::uniform_int_distribution<size_t>(0, Size-1)(rng) : 0;
        return splice(Data, Size, MaxSize, pos, key, totalLen);
    }
    case 2: { // patch_type_id: splice a known QMetaType typeId at a random position
        const auto *tid = kTypeIds[rng() % std::size(kTypeIds)];
        const size_t pos = Size > 1 ? std::uniform_int_distribution<size_t>(0, Size-1)(rng) : 0;
        return splice(Data, Size, MaxSize, pos, tid, 4);
    }
    case 3: { // patch_element_type: splice a valid ElementType as quint64 big-endian
        if (!g_elemTypePatches.empty()) {
            const auto &ep = g_elemTypePatches[rng() % g_elemTypePatches.size()];
            const size_t pos = Size > 1 ? std::uniform_int_distribution<size_t>(0, Size-1)(rng) : 0;
            return splice(Data, Size, MaxSize, pos, ep.data(), ep.size());
        }
        break;
    }
    case 4: { // patch_header: write WPCF magic + version at byte 0 (whole preamble)
        const auto &vp = kVersions[rng() % std::size(kVersions)];
        // Full preamble: 4-byte magic + 12-byte version = 16 bytes
        constexpr uint8_t magic[4] = {0x57, 0x50, 0x43, 0x46}; // "WPCF"
        if (MaxSize >= 16) {
            const size_t newSize = std::max(Size, size_t(16));
            if (newSize > Size && newSize <= MaxSize) {
                std::memset(Data + Size, 0, newSize - Size);
            }
            std::memcpy(Data, magic, 4);
            std::memcpy(Data + 4, vp.bytes, 12);
            return std::max(newSize, Size);
        }
        break;
    }
    case 5: { // patch_item_type: splice a GraphicElement or QNEConnection type discriminator
        // These 4-byte big-endian values are the switch keys in Serialization::deserialize().
        // Splicing them into the payload helps the fuzzer discover the element/connection
        // loading code paths from raw-byte mutations.
        const auto *it = kItemTypes[rng() % std::size(kItemTypes)];
        const size_t pos = Size > 1 ? std::uniform_int_distribution<size_t>(0, Size-1)(rng) : 0;
        return splice(Data, Size, MaxSize, pos, it, 4);
    }
    case 6: { // patch_element_seq: splice 12-byte GraphicElement::Type + ElementType quint64
        // A single mutation that creates a complete element header (type discriminator
        // + element type) so the fuzzer reaches element-loading code in one step.
        if (!g_elemSeqPatches.empty()) {
            const auto &seq = g_elemSeqPatches[rng() % g_elemSeqPatches.size()];
            const size_t pos = Size > 1 ? std::uniform_int_distribution<size_t>(0, Size-1)(rng) : 0;
            return splice(Data, Size, MaxSize, pos, seq.data(), seq.size());
        }
        break;
    }
    case 7: { // patch_appearance_entry: splice a valid appearances list entry with a skinName key
        // Splicing this 45-byte sequence at the position right after the output port list
        // exercises the skinName reading path in GraphicElement::loadNewFormat.
        const size_t pos = Size > 1 ? std::uniform_int_distribution<size_t>(0, Size-1)(rng) : 0;
        return splice(Data, Size, MaxSize, pos, kAppearanceEntry, sizeof(kAppearanceEntry));
    }
    default:
        break;
    }

    // fallback: let libFuzzer do its own mutation
    return LLVMFuzzerMutate(Data, Size, MaxSize);
}

extern "C" int LLVMFuzzerInitialize(int *argc, char ***argv)
{
    qputenv("QT_QPA_PLATFORM", "offscreen");
    qputenv("QT_IM_MODULES", "none");

    Comment::setVerbosity(-1);
    Application::interactiveMode = false;
    Application::renderingEnabled = false;
    Application::migrationEnabled = false;

    g_fuzzer_argc = *argc;
    g_fuzzer_argv = *argv;
    g_app = new QApplication(g_fuzzer_argc, g_fuzzer_argv);

    buildElemTypePatches();

    return 0;
}

extern "C" int LLVMFuzzerTestOneInput(const uint8_t *data, size_t size)
{
    // Drain Qt's deferred-delete queue on every exit path.  Audio elements
    // (Buzzer, AudioBox) queue DeferredDelete events for the audio backend's
    // internal QObject helpers when destroyed; without spinning the event
    // loop, those helpers stay alive and hold PipeWire stream FDs open
    // across iterations, exhausting the audio subsystem after ~1000 inputs.
    auto drainQtEvents = qScopeGuard([] {
        QCoreApplication::sendPostedEvents(nullptr, QEvent::DeferredDelete);
        QCoreApplication::processEvents();
    });

    const QByteArray buffer = QByteArray::fromRawData(reinterpret_cast<const char *>(data),
                                                      static_cast<int>(size));
    QDataStream stream(buffer);

    QVersionNumber version;
    try {
        version = Serialization::readPandaHeader(stream);
    } catch (...) {
        return 0;
    }

    WorkSpace workspace;
    try {
        workspace.load(stream, version, /*contextDir=*/QString());
    } catch (...) {}

    workspace.scene()->simulation()->update();

    return 0;
}
