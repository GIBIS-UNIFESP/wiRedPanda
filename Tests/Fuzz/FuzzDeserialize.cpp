// Element type patches as quint64 big-endian (8 bytes each).
std::vector<std::vector<uint8_t>> g_elemTypePatches;

// Element sequence patches: 4-byte GraphicElement::Type + 8-byte ElementType.
// Splicing a 12-byte sequence at once creates a valid element header that
// guides the fuzzer to the element-loading code path in one mutation step.
std::vector<std::vector<uint8_t>> g_elemSeqPatches;

// Appearances list entry: quint32(count=1) + QMap<"skinName": "test"> in Qt_5_12 wire format.
//
// Wire layout (45 bytes):
//   [0..3]   quint32(1)      — appearances count = 1
//   [4..7]   quint32(1)      — QMap size = 1 entry
//   [8..11]  quint32(16)     — "skinName" UTF-16 byte-length
//   [12..27] UTF-16-BE       — "skinName"
//   [28..31] quint32(10)     — QVariant typeId = QString
//   [32]     0x00            — isNull = false
//   [33..36] quint32(8)      — "test" UTF-16 byte-length
//   [37..44] UTF-16-BE       — "test"
//
// Splicing this sequence at the right position (after the output port list) exercises
// the skinName reading path in GraphicElement::loadNewFormat (lines 338-342 of
// GraphicElementSerializer.cpp) which was dark in all raw-mutation corpus entries.
constexpr uint8_t kAppearanceEntry[] = {
    0,0,0,1,                                          // appearances count = 1
    0,0,0,1,                                          // QMap size = 1 entry
    0,0,0,16,                                         // "skinName" UTF-16 byte-length
    0,'s',0,'k',0,'i',0,'n',0,'N',0,'a',0,'m',0,'e', // "skinName" as UTF-16-BE
    0,0,0,10,                                         // QVariant typeId = QString (10)
    0,                                                // isNull = false
    0,0,0,8,                                          // "test" UTF-16 byte-length
    0,'t',0,'e',0,'s',0,'t',                          // "test" as UTF-16-BE
};

void buildElemTypePatches()
{
    // All valid ElementType enum values
    static const quint64 etypes[] = {
        1,  // InputButton
        2,  // InputSwitch
        3,  // Led
        4,  // Not
        5,  // And
        6,  // Or
        7,  // Nand
        8,  // Nor
        9,  // Clock
        10, // Xor
        11, // Xnor
        12, // InputVcc
        13, // InputGnd
        14, // Display7
        15, // DLatch
        16, // JKLatch
        17, // DFlipFlop
        18, // JKFlipFlop
        19, // SRFlipFlop
        20, // TFlipFlop
        21, // InputRotary
        22, // IC
        23, // Node
        24, // Mux
        25, // Demux
        26, // Buzzer
        27, // Display14
        28, // Text
        29, // Line
        30, // TruthTable
        31, // AudioBox
        32, // SRLatch
        33, // Display16
    };
    for (quint64 v : etypes) {
        // 8-byte quint64 element-type patch
        std::vector<uint8_t> patch(8);
        patch[0] = uint8_t(v >> 56); patch[1] = uint8_t(v >> 48);
        patch[2] = uint8_t(v >> 40); patch[3] = uint8_t(v >> 32);
        patch[4] = uint8_t(v >> 24); patch[5] = uint8_t(v >> 16);
        patch[6] = uint8_t(v >>  8); patch[7] = uint8_t(v);
        g_elemTypePatches.push_back(patch);

        // 12-byte sequence: GraphicElement::Type (0x00010003) + ElementType quint64
        // Splicing this creates a complete element header in one mutation step.
        std::vector<uint8_t> seq(12);
        seq[0] = 0; seq[1] = 1; seq[2] = 0; seq[3] = 3; // GraphicElement::Type = 65539
        seq[4]  = patch[0]; seq[5]  = patch[1]; seq[6]  = patch[2]; seq[7]  = patch[3];
        seq[8]  = patch[4]; seq[9]  = patch[5]; seq[10] = patch[6]; seq[11] = patch[7];
        g_elemSeqPatches.push_back(std::move(seq));
    }
}

// Splice `patch` (of `patchLen` bytes) at position `pos` inside [Data, Data+Size],
// without exceeding MaxSize.  Returns new size.
size_t splice(uint8_t *Data, size_t Size, size_t MaxSize,
              size_t pos, const uint8_t *patch, size_t patchLen)
{
    if (pos > Size || patchLen == 0) return Size;
    const size_t tail   = Size - pos;
    const size_t newEnd = std::min(pos + patchLen + tail, MaxSize);
    // Shift tail right to make room (may truncate if MaxSize is tight)
    const size_t keep = std::min(tail, newEnd - std::min(pos + patchLen, newEnd));
    if (pos + patchLen < newEnd)
        std::memmove(Data + pos + patchLen, Data + pos, keep);
    const size_t copy = std::min(patchLen, newEnd - pos);
    std::memcpy(Data + pos, patch, copy);
    return newEnd;
}

