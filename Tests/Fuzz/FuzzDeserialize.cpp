/** \file
 * \brief libFuzzer harness for the .panda deserializer.
 *
 * Drives the same code path as File > Open: header read, embedded-IC blob
 * registry parsing, the element/connection stream loop in
 * Serialization::deserialize(), and the Scene::addItem() side effects that
 * follow.  Run under ASan + UBSan to catch the H2/HC family of bugs on inputs
 * no human would hand-write.  See Tests/Fuzz/README.md for build and run
 * instructions.
 *
 * Custom mutator (LLVMFuzzerCustomMutator):
 *   libFuzzer's default byte-level mutations are too coarse for the .panda
 *   binary format.  Random bit-flips almost always corrupt the WPCF magic or
 *   a length field, causing an early throw before reaching interesting code.
 *   The custom mutator applies one structure-aware operation per call so that
 *   the corpus stays parseable while still reaching deep code paths:
 *
 *     - patch_version:    replace the 8-byte version field with a known-valid one
 *     - patch_map_key:    splice a known map key (UTF-16-BE with length prefix)
 *                         at a random position inside the payload
 *     - patch_type_id:    splice a known QMetaType wire typeId at a random position
 *     - patch_element_type: replace one element-type string inside the payload
 *     - fallback:         call LLVMFuzzerMutate (libFuzzer's own mutator)
 *
 *   The mutator operates on raw bytes so it is safe even when the input does
 *   not parse — it simply writes into whatever slice it picked.
 */

